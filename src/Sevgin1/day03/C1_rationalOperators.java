package Sevgin1.day03;

public class C1_rationalOperators {

    public static void main(String [] args){
           //create variables
           int a=10;
           int b=15;


           //== equal operator
           System.out.println(a==b); // 10==15 false
           // != (opposite equal ---> not equal)
           System.out.println(a != b); // 10 !=15 true
           // > operator
           System.out.println(a >b); // 10 > 15 false
           // < operator
           System.out.println(a < b);// 10 < 25 true
           // >= operator
           System.out.println( a >= b); // 10 >= 25 false
           // <= operator
           System.out.println( a<= b); // 10 <= 25 true
    }
}
