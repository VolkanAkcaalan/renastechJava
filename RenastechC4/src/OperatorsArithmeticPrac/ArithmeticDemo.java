package OperatorsArithmeticPrac;

public class ArithmeticDemo {

    public static void main(String[] args) {
        int a= 10 ;
        int b=20 ;

        int add= a+b ;
        int subtract= a-b ;
        int multiply= a*b ;
        int divide= a/b;
        int modules=a%b;

        System.out.println(add +"\n"+subtract+"\n"+multiply+"\n"+divide+"\n"+modules);
    }
}
